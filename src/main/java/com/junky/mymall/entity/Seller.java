package com.junky.mymall.entity;


public class Seller {

    private String sellerId;
    private String sellerName;
    private String sellerPhone;
    private String sellerPassword;
    private String sellerIcon;
    private String sellerDesc;
    private String sellerAddress;


    public String getSellerId() {
        return sellerId;
    }

    public void setSellerId(String sellerId) {
        this.sellerId = sellerId;
    }


    public String getSellerName() {
        return sellerName;
    }

    public void setSellerName(String sellerName) {
        this.sellerName = sellerName;
    }


    public String getSellerPhone() {
        return sellerPhone;
    }

    public void setSellerPhone(String sellerPhone) {
        this.sellerPhone = sellerPhone;
    }


    public String getSellerPassword() {
        return sellerPassword;
    }

    public void setSellerPassword(String sellerPassword) {
        this.sellerPassword = sellerPassword;
    }


    public String getSellerIcon() {
        return sellerIcon;
    }

    public void setSellerIcon(String sellerIcon) {
        this.sellerIcon = sellerIcon;
    }


    public String getSellerDesc() {
        return sellerDesc;
    }

    public void setSellerDesc(String sellerDesc) {
        this.sellerDesc = sellerDesc;
    }


    public String getSellerAddress() {
        return sellerAddress;
    }

    public void setSellerAddress(String sellerAddress) {
        this.sellerAddress = sellerAddress;
    }

}
